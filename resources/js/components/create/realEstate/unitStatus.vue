<template>
    <!--  create   -->
    <b-modal
        id="unit-satatus-create"
        :title="getCompanyKey('unitstatus_create_form')"
        title-class="font-18"
        body-class="p-4 "
        :hide-footer="true"
        @show="resetModal"
        @hidden="resetModalHidden"
    >
        <form>
            <div class="mb-3 d-flex justify-content-end">
                <b-button
                    variant="success"
                    :disabled="!is_disabled"
                    @click.prevent="resetForm"
                    type="button"
                    :class="['font-weight-bold px-2', is_disabled ? 'mx-2' : '']"
                >
                    {{ $t("general.AddNewRecord") }}
                </b-button>
                <template v-if="!is_disabled">
                    <b-button
                        variant="success"
                        type="button"
                        class="mx-1"
                        v-if="!isLoader"
                        @click.prevent="AddSubmit"
                    >
                        {{ $t("general.Add") }}
                    </b-button>

                    <b-button variant="success" class="mx-1" disabled v-else>
                        <b-spinner small></b-spinner>
                        <span class="sr-only">{{ $t("login.Loading") }}...</span>
                    </b-button>
                </template>
                <!-- Emulate built in modal footer ok and cancel button actions -->

                <b-button
                    variant="danger"
                    type="button"
                    @click.prevent="resetModalHidden"
                >
                    {{ $t("general.Cancel") }}
                </b-button>
            </div>
            <div class="row">
                <div class="col-md-12">
                    <div class="form-group">
                        <label for="field-1" class="control-label">
                            {{ getCompanyKey("unitstatus_name_ar") }}
                            <span class="text-danger">*</span>
                        </label>
                        <div dir="rtl">
                            <input
                                type="text"
                                class="form-control arabicInput"
                                data-create="1"
                                v-model="$v.create.name.$model"
                                :class="{
                            'is-invalid': $v.create.name.$error || errors.name,
                            'is-valid': !$v.create.name.$invalid && !errors.name,
                          }"
                                @keyup="arabicValue(create.name)"
                                id="field-1"
                            />
                        </div>
                        <div v-if="!$v.create.name.minLength" class="invalid-feedback">
                            {{ $t("general.Itmustbeatleast") }}
                            {{ $v.create.name.$params.minLength.min }}
                            {{ $t("general.letters") }}
                        </div>
                        <div v-if="!$v.create.name.maxLength" class="invalid-feedback">
                            {{ $t("general.Itmustbeatmost") }}
                            {{ $v.create.name.$params.maxLength.max }}
                            {{ $t("general.letters") }}
                        </div>
                        <template v-if="errors.name">
                            <ErrorMessage
                                v-for="(errorMessage, index) in errors.name"
                                :key="index"
                            >{{ errorMessage }}</ErrorMessage
                            >
                        </template>
                    </div>
                </div>
                <div class="col-md-12">
                    <div class="form-group">
                        <label for="field-2" class="control-label">
                            {{ getCompanyKey("unitstatus_name_en") }}
                            <span class="text-danger">*</span>
                        </label>
                        <div dir="ltr">
                            <input
                                type="text"
                                class="form-control englishInput"
                                data-create="2"
                                v-model="$v.create.name_e.$model"
                                :class="{
                            'is-invalid': $v.create.name_e.$error || errors.name_e,
                            'is-valid': !$v.create.name_e.$invalid && !errors.name_e,
                          }"
                                @keyup="englishValue(create.name_e)"
                                id="field-2"
                            />
                        </div>
                        <div v-if="!$v.create.name_e.minLength" class="invalid-feedback">
                            {{ $t("general.Itmustbeatleast") }}
                            {{ $v.create.name_e.$params.minLength.min }}
                            {{ $t("general.letters") }}
                        </div>
                        <div v-if="!$v.create.name_e.maxLength" class="invalid-feedback">
                            {{ $t("general.Itmustbeatmost") }}
                            {{ $v.create.name_e.$params.maxLength.max }}
                            {{ $t("general.letters") }}
                        </div>
                        <template v-if="errors.name_e">
                            <ErrorMessage
                                v-for="(errorMessage, index) in errors.name_e"
                                :key="index"
                            >{{ errorMessage }}</ErrorMessage
                            >
                        </template>
                    </div>
                </div>
                <div class="col-md-12">
                    <div class="form-group">
                        <label class="mr-2">
                            {{ getCompanyKey("unitstatus_default") }}
                            <span class="text-danger">*</span>
                        </label>
                        <select
                            class="custom-select mr-sm-2"
                            data-create="4"
                            v-model="$v.create.is_default.$model"
                            :class="{
                          'is-invalid': $v.create.is_default.$error || errors.is_default,
                          'is-valid':
                            !$v.create.is_default.$invalid && !errors.is_default,
                        }"
                        >
                            <option value="">{{ $t("general.Choose") }}...</option>
                            <option value="1">{{ $t("general.Default") }}</option>
                            <option value="0">{{ $t("general.Nondefault") }}</option>
                        </select>
                        <template v-if="errors.is_default">
                            <ErrorMessage
                                v-for="(errorMessage, index) in errors.is_default"
                                :key="index"
                            >{{ errorMessage }}</ErrorMessage
                            >
                        </template>
                    </div>
                </div>
                <div class="col-md-12">
                    <div class="form-group">
                        <label class="mr-2">
                            {{ getCompanyKey("unitstatus_status") }}
                            <span class="text-danger">*</span>
                        </label>
                        <b-form-group
                            :class="{
                          'is-invalid': $v.create.is_active.$error || errors.is_active,
                          'is-valid': !$v.create.is_active.$invalid && !errors.is_active,
                        }"
                        >
                            <b-form-radio
                                class="d-inline-block"
                                v-model="$v.create.is_active.$model"
                                name="some-radios"
                                value="active"
                            >{{ $t("general.Active") }}</b-form-radio
                            >
                            <b-form-radio
                                class="d-inline-block m-1"
                                v-model="$v.create.is_active.$model"
                                name="some-radios"
                                value="inactive"
                            >{{ $t("general.Inactive") }}</b-form-radio
                            >
                        </b-form-group>
                        <template v-if="errors.is_active">
                            <ErrorMessage
                                v-for="(errorMessage, index) in errors.is_active"
                                :key="index"
                            >{{ errorMessage }}</ErrorMessage
                            >
                        </template>
                    </div>
                </div>
            </div>
        </form>
    </b-modal>
    <!--  /create   -->
</template>

<script>
import Layout from "../../../views/layouts/main";
import PageHeader from "../../Page-header";
import Switches from "vue-switches";
import ErrorMessage from "../../widgets/errorMessage";
import loader from "../../loader";
import {maxLength, minLength, required} from "vuelidate/lib/validators";
import adminApi from "../../../api/adminAxios";
import Swal from "sweetalert2";
import transMixinComp from "../../../helper/translation-comp-mixin";
import {arabicValue, englishValue} from "../../../helper/langTransform";

export default {
    name: "unitStatus",
    components: {
        Layout,
        PageHeader,
        Switches,
        ErrorMessage,
        loader,
    },
    props: {
        companyKeys:{
            default:[]
        },
        defaultsKeys:{
            default:[]
        },
    },
    mixins: [transMixinComp],
    data() {
        return {
            isLoader: false,
            create: {
                name: "",
                name_e: "",
                is_active: "active",
                is_default: 1,
            },
            errors: {},
            is_disabled: false,
        };
    },
    validations: {
        create: {
            name: { required, minLength: minLength(2), maxLength: maxLength(100) },
            name_e: { required, minLength: minLength(2), maxLength: maxLength(100) },
            is_active: { required },
            is_default: { required },
        },
    },
    methods: {
        resetModalHidden() {
            this.create = {
                name: "",
                name_e: "",
                is_active: "active",
                is_default: 1,
            };
            this.$nextTick(() => {
                this.$v.$reset();
            });
            this.errors = {};
            this.$bvModal.hide(`unit-satatus-create`);
        },
        /**
         *  hidden Modal (create)
         */
        resetModal() {
            this.create = {
                name: "",
                name_e: "",
                is_active: "active",
                is_default: 1,
            };
            this.$nextTick(() => {
                this.$v.$reset();
            });
            this.errors = {};
        },
        /**
         *  create countrie
         */
        resetForm() {
            this.create = {
                name: "",
                name_e: "",
                is_active: "active",
                is_default: 1,
            };
            this.$nextTick(() => {
                this.$v.$reset();
            });
            this.errors = {};
            this.is_disabled = false;
        },
        AddSubmit() {
            if (!this.create.name) {
                this.create.name = this.create.name_e;
            }
            if (!this.create.name_e) {
                this.create.name_e = this.create.name;
            }
            this.$v.create.$touch();

            if (this.$v.create.$invalid) {
                return;
            } else {
                this.isLoader = true;
                this.errors = {};
                let data = {
                    name: this.create.name,
                    name_e: this.create.name_e,
                    is_active: this.create.is_active,
                    is_default: this.create.is_default,
                };
                adminApi
                    .post(`real-estate/unit-statuses`, data)
                    .then((res) => {
                        this.is_disabled = true;
                        this.$emit('created');
                        setTimeout(() => {
                            Swal.fire({
                                icon: "success",
                                text: `${this.$t("general.Addedsuccessfully")}`,
                                showConfirmButton: false,
                                timer: 1500,
                            });
                        }, 500);
                    })
                    .catch((err) => {
                        if (err.response.data) {
                            this.errors = err.response.data.errors;
                        } else {
                            Swal.fire({
                                icon: "error",
                                title: `${this.$t("general.Error")}`,
                                text: `${this.$t("general.Thereisanerrorinthesystem")}`,
                            });
                        }
                    })
                    .finally(() => {
                        this.isLoader = false;
                    });
            }
        },
        arabicValue(txt){
            this.create.name = arabicValue(txt);
        },

        englishValue(txt){
            this.create.name_e = englishValue(txt);
        }
    }
}
</script>

